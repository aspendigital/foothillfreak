            <h1>Ski Touring in the Uintas  </h1>
            <h6>Saturday, November 25th, 2006</h6>
            <p>The goal was to find the Castle Peak Yurt and to summit Castle
              Peak. We achieved neither, but we  did manage to ski
              tons of breakable crusts and avoid the throngs of backcountry
              skiers that were  either running errands or Christmas shopping
              in the valley.</p>
            <p>Actually, we just wanted to walk the dogs, get some exercise,
              and do some recon for future Uinta exploits. Seeing mountains I'd
              never seen before, navigating the tricky forests, and being with
              quality friends and fidos was well worth the extra trekking. </p>
            <p><img src="DSC02853.jpg" alt="Ski Tour in the Uintas" width="600" height="450" /></p>
            <p>As you can see here, there was plenty of snow.</p>
            <p><img src="DSC02860.jpg" alt="Long road to Castle Yurt" width="600" height="462" /></p>
            <p>And as we got higher, there was even more snow. </p>
            <p><img src="DSC02862.jpg" alt="skiing the Uintas" width="600" height="458" /></p>
            <p>Most of the pitches that were steep enough for exciting skiing
              looked like this.  </p>
            <p><img src="DSC02868.jpg" alt="Star Child back pack" width="455" height="646" /></p>
            <p>After about 7 miles of spongy breakable crust, or what I call
              mattress protector crust, Star Child had a small breakdown and
              refused to run any further. Foothill gave her a short lift to the
              more supportable crusts and she was good to go for the last 3 miles.</p>
            <p>Other events worth noting:  </p>
            <ul>
              <li>Low angle forests that were challenging to navigate. </li>
              <li>Princesses' loose ski tobogganing down an icy rut and luckily
                catching it.</li>
              <li>Skating across one of the three lakes we stumbled on.</li>
              <li>The road that went on forever and ever. </li>
            </ul>
            <p>&nbsp;</p>
            <p>Reasons to go back to the Uintas:</p>
            <ul>
              <li>Go for a tour on light touring skis and really knock
                back some miles.</li>
              <li>Go when there is more snow, less crusts, for obvious reasons. </li>
              <li>Do a traverse to Smith and Morehouse Canyon: guaranteed adventure.</li>
              <li>Stay at the yurt, bring a gps (the guide book says to follow
                old ski tracks to the yurt... What ski tracks???) </li>
            </ul>
