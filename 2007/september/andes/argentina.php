            <h6>September, 2007</h6>
         
<p>One of the amazing things about the trip was how I looked at a road map and
  figured out a loop we could drive down the Argentinian side of the Andes, then
  up the Chilean side. It looked good on paper, but there were some doubts:</p>
<ul>
  <li>Would
    the mountain passes be open? </li>
  <li>Would we be able to cross the border, especially with a rental car?</li>
  <li>Would the roads be buried under feet of snow? </li>
  <li>Is the road a four lane highway, or a washboard gravel nightmare?</li>
  <li>Would driving a little piece of road on the map take an hour or a day?</li>
  </ul>
<p>The answers to these questions all came in good time. To put it simply, things
  were both easier, and more difficult than we had imagined. Roads were much
  worse in Argentina than I had imagined, so it was reassuring to
  have a 4wd vehicle with a full sized spare for the times we crossed through
  the middle of nowhere to the other middle of nowhere. Next time I rent a car
  for a trip like this, I'd be tempted to enquire if the camioneta had a full
  size spare tire. Then again, that may raise some suspicions.</p>
<p>If you don't know any Spanish, I wouldn't reccommend driving a rental car
  from Chile to Argentina, or vice versa. The paperwork and the ordeal at 
  not one, but several stops from one country to the next would be rather overwhelming,
  especially at the smaller bordercrossings where some spanish skilz are expected.
  Then again, in the end I just ended up putting all the important paperwork
  into one folder and just handing it over...</p>
<p><img src="images/DSC03984.jpg" /></p>
<p>Crossing from Portillo, Chile to Mendoza, Argentina was truly one of the worst
  days of my life. Part of the diffulcity was being stuck in a 7 hour line of
  traffic waiting to pass through la aduana (customs). If the miserable day had
  a highlight, it was being at the foot of the highest peak in North and South
  Americas, Aconcagua (22,841 ft, barely visible here in the far distance behind
  the cross to the right. <a href="http://en.wikipedia.org/wiki/Aconcagua">More
  on Aconcagua.</a></p>
<p><img src="images/DSC00335.jpg" alt="Aconcagua traffic" width="600" height="436" /></p>
<p>I'd really rather not remember this image of the 7 hour traffic jam, but I'll
  post it anyway. Maybe some day I'll be over it...</p>
