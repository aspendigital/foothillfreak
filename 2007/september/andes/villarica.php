<p>  The first guy I talked to said that there was nowhere to stay. When I came
  out of the bathroom a quiet guy approached me, said that he was the sole
  night watchman and that we could stay and sleep on the floor as long as we
  provided the nights entertainment. With my limited spanish skills this proved
  more difficult than I had anticipated, but sleeping directly beneath the red
  glowing Villarica was a true highlight.</p>
