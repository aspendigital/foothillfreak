            <h6>September, 2007</h6>
         
            <p>At this point of our trip, we had grown comfortable with pulling
              up to a roadside set of caba&ntilde;as late at night and taking
              whatever they had to offer. I was used
              to cold showers and beds with sheets  so thin that you could
              clearly see through them to the mattress&#8212;which,
            generally, is not a good thing to see. </p>
            <p>When we pulled up to El Medina
            it was instantly obvious that this place was different. I'm not just
              talking about how the second I got our of the car I was attacked
              by a posse of twelve small dogs while Choriza and Fu laughed safely
              from inside the vehicle. The difference was the dog's master and
              owner of El Medina, Emilio. He eventually
              called off the dogs and gave us a tour of his little bit of paradise
              that he'd built at the foot of the Andes. An Italian/French expatriate,
              he came to South America to create a more simple, fufilling life
              more in tune with the land... And he's doing a beautiful job of
              it!
            <p>There were two caba&ntilde;as, and
              they were beautiful. They had a very European rustic cottage feel
              to them and were built entirely by hand. Each door was rough hewn,
              slotted and pegged together. The chandelier was a recycled tandem
              ox harness with candle-holding wine glasses attached with leather
              straps. The masonry seemed not to follow a pattern so much as
              the bricklayer's creative mood at the moment they were layed. Everywhere
              you looked was a new pleasure, a new display of human handwork,
              ingenuity and craftsmanship.
            <p>And staying there was cheap, too!            
