    <h2>Day 1:
      Fritschi Freeride. The do it all binding.</h2>
    <p><img src="DSC04267.jpg" alt="Fritschi Freeride Bindings at Alta" width="600" height="273" /></p>
    <h2>Fritschi Freeride Review in 5 adjectives.</h2>
    <p>Easy. Bomber. Solid. Simple. all-areas. </p>
    <h2>More about the Fritschi</h2>
    <p>After years of watching folks on their Fritschi's lumbering along with
              their Frankenstein walk, I believed all the talk about the binding's
              lack of &quot;anatomical pivot points&quot; and the un-natural
              stride it creates. But when I first effortlessly clicked in to
              the Freerides, I liked the bindings right away. </p>
    <p>Granted, The walking did initally seem a little less fluid than what I'm
      used to, but after a few steps I really didn't notice at all. What I did
      notice was how easy it was to get in and out of the ski the multiple times
              that I came to snowless patches of dry ground. This small convenience
              lower on the mountain was a true pleasure once
              the slope angle steepened. When switching from skinning to boot-packing
      on a steep slope, just getting out of your skis can be a real precarious
      situation. Not so with the Fritschis. Just pop the well cupped heel lever
      with your pole tip and your boot comes out nicely.</p>
    <p>I picked a skin route with various steepnesses to try out the
              different heel lifts. It was easy to use the end of the ski pole
      to select the lower settings, including lock-down, but to reach the highest
      setting, I found it easiest to flip the pole around and to use the lip
      of the handle. </p>
    <p>One of my biggest tests is how easy it is to go into lock-down mode when
      your ready to ski. This is where the binding has to transition flawlessly.
      After the climb, its critical that you can transition into descent
      mode without taking your skis off. Otherwise, your friends might beat
      you to the freshies or even worse, someone might see you futzing with
      gear and mistake you for a split-boarder. </p>
    <p>The few times I transitioned,
      the Fritschi locked down just fine. Once when I picked up the ski there
      was a bit of snow under the heel lock-down area preventing a lock down,
      but  I knocked it out just fine. Not sure if this area can gum up in
      powdery or sticky snow... Hopefully we'll have a dump so I can find out!</p>
    <p>Next I skinned up to a short boot-pack. It was almost long enough to strap
      my skis on to my pack, but feeling lazy, I decided to just shoulder them.
              I was plesantly suprised to discover how the large, rounded toe-piece
              was quite comfortable where it rested on my shoulder, and although
      the ascent lasted about 15 minutes,  the skis were always comfortable
      on my shoulder and the thick binding kept the skis in place.</p>
            <p>Again, when I was ready to switch into my skis for the run down,
              the ease of getting into the skis was a big confidence booster.
              Just two clicks and I was ready to go. The first part of my run
              was heinously inconsistent crusts and wind board. I thought that
              the extra height this binding adds under foot might hinder my balance
              through the crud, but instead it felt quite powerful and stable.
              When the off-pist changed to soft, groomed pist, that extra height
              really allowed me to roll the fat boards over onto their edge,
              creating an energetic and carvy feel that  truly suprised
              me. Lower on the mountain, when the snow turned into snow-blown
              ice, I lost that carvable sensation, but the bindings continued
              to be a secure link to the ski. Someday they'll invent a
              binding that makes the ice go away.</p>
            <h2>Stats:</h2>
