      ski from the tops of &quot;the seven summits&quot;.</p>
    <h2>More About the TLT Vertical ST</h2>
    <p>Requires more thinking than the others: Such as locking and unlocking
      your toe piece for climbing or skiing. </p>
    <p>Requires about one more step per transition than the others.</p>
    <p>Requires bending
      over more than the other bindings. Not a problem if you're buckeling or
      unbuckeling your boots anyways.</p>
    <p>New volcano shape totally rules for guiding pole tip into the slot for
      heel lifter adjustments.</p>
    <p>Nice &quot;snap&quot; to heel adjustments that makes finding positions easy.</p>
    <p>Longer toe piece lever makes locking in even easier.</p>
    <p>Finally fixed the crampon problem by adding a steel inner-structure.</p>
    <p>&nbsp;</p>
    <h2>Stats</h2>
