    <h2>Naxo 21 Review in 3 sentences:</h2>
    <p>The 2 pivot points of the Naxo do allow a fluid stride, but it feels clunky
      to someone used to single pivot bindings. On a steep, icy traverse, the
      binding has a fair amount of play, making it hard to sink an edge. Skiing
      down it was a solid performer, similar to the fritchi.</p>
    <h2>More About the Naxos</h2>
    <ul>
      <li>Climbing posts not high enough, but operate easily  by
        either prying or wacking with the pole end.</li>
      <li>Fluid walking motion, but it clunked a lot. This clunking seemed to
        go away as you get used to it and/or once the binding &quot;taught&quot; me
        how to walk inorder to avoid the clunk.</li>
      <li>Easy in and out, easy to step into in precarious spots, breaks worked
        well.</li>
      <li>nice positive positioning for the heel lifts.</li>
      <li>smooth step in and out. </li>
      <li>ski breaks worked for the fat boards.</li>
      <li>Friend called the clicking noise obnaxous</li>
    </ul>
    <h2>Stats</h2>
    <p>Weight, one binding with screws: 43.2oz, or 2lbs 11.2oz, or pretty close
      to 3lbs each.<br />
      Heel lifts: 2 levels. Operated by prying and wacking with pole end.<br />
       Heel lift, medium: 4&deg;<br />
    Heel lift, tall: 10&deg;<br />
    Ball of foot height: 2.9cm<br />
    Heel height: 3.3cm<br />
    </p>
    
