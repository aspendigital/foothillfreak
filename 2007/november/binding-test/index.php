    <h1>AT Binding Review</h1>
    <h2>Day 1:
      Fritschi Freeride</h2>
    <p><img src="DSC04270.jpg" alt="Fritschi Freeride Bindings at Alta" width="600" height="450" /></p>
    <p>After years of watching folks on Fritschi's lumbering along with
              their frankenstein walk, I believed all the talk about the binding's
              lacking of &quot;anatomical pivot points&quot; and &quot;natural
              stride&quot;. But when I first effortlessly clicked in, I like
              these bindings right away. </p>
            <p>The walking was a little less fluid than what I'm used to, but
              after a few steps I really didn't notice at all. What I did notice
              was how easy it was to get in and out of the ski the multiple times
              that I came to snowless patches of dry ground. This small convenience
              lower on the mountain felt like a real confidence builder once
              the slope angle steepened. When you're on a steep slope and you
              switch from skinning to boot-packing, just getting out of your
              skis can be a real precarious situation. Not so with the Fritschi.
              Just pop the well cupped heel lever and you're out.</p>
            <p>I picked a skin route with various steepnesses to try out the
              different heel lifts. </p>
            <p>I came to a short boot-pack, almost long enough to strap my skis
              in to my pack, but feeling lazy, I decided to just shoulder them.
              I was plesantly suprised to discover the the large, rounded toe-piece
              was quite comfortable where it rested on my shoulder, and tho the
              ascent was around 250 vft, the skis were always comfortable on
              my shoulder. </p>
            <p>Again, when I was ready to switch into my skis for the run down,
              the ease of getting into the skis was a big confidence booster.
              Just two clicks and I was ready to go. The first part of my run
              was heinously inconsistent crusts and wind board. I thought that
              the extra height this binding adds under foot might hinder my balance
              through the crud, but instead it felt quite powerful and stable.
              When the off-pist changed to soft, groomed pist, that extra height
              really allowed me to roll the fat boards over onto their edge,
              creating an energetic and carvy feel that was fun and truly suprised
              me. Lower on the mountain, when the snow turned into a more impenetrable
              ice, I lost that sensation, but the bindings still felt like a
              secure link to the ski. </p>
            <p>Maybe someday they'll invent a binding that makes the ice go away... </p>
