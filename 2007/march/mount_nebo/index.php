              bowls. </p>
            <p><img src="DSC03551.jpg" alt="skiing up mount Nebo" width="550" height="688" /></p>
            <p>Thankfully, the ascent of Nebo from the west is quite direct.
              There are only a couple tiny meadows to cross so most of the time
              you're in full climb mode. Seems like every time you look over
              your shoulder to the valley  you've gained another 1000 feet, because
              you have.</p>
            <p><img src="DSC03549.jpg" alt="View of Nebo Chutes" width="600" height="438" /></p>
            <p>The Pole Canyon Bowl is really quite huge. It's hard to think
              of an area in the wasatch tri-canyons that compares. This shot
              is looking up the super-wide-right-chute which ends on the ridge,
              about 200ft from the summit. </p>
            <p><img src="DSC03557.jpg" alt="The highest point of Nebo's Pole Canyon Bowl" width="600" height="450" /></p>
            <p>The super-wide-right-chute was super full of 
              graupel that was buried under a super stiff windslab. We bypassed
              it to go higher up the apron. <br />
            </p>
            <p><img src="DSC03564.jpg" alt="Nebo left chute" width="600" height="450" /></p>
            <p>This is looking up the tasty, rock sluffing  left-chute. Much
              narrower than it's brother to the right. I dug a snow pit here
              and discovered a foot and a half of some of the largest facets
              I've ever seen. We decided to simply session the bowl. </p>
            <p><img src="DSC03565.jpg" alt="Skiing Nebo Apron" width="600" height="434" /></p>
            <p>Powder Princess makes the first few turns of her 1500 foot, non-stop,
              crust-busting, corn-carving run that seemed to last for an eternity. </p>
