            <h2>The Tupperware Fake-Out </h2>
            <p>At work we have a name for when someone  offers you &quot;home-made&quot;
              cookies, but after you take a bite, you discover that they're store-bought:
              it's called a Tupperware Fake-Out. The strategy often involves
              buying the cheapest manufactured cookies you can,
              ripping them out of their plastic corporate packaging, and placing
              them in a homey tupperware, or even better, on a grandma-ish plate
              covered with cellophane and ribbons. The sight of the faux home
              made cookies is visually pleasing, possibly bringing to mind fond
              memories of grandparents, chewy fresh chocolate, and smooth, creamy
              butter. But a bite of tupperware fake-out cookies reveals a stiff,
              chalky substitute. You thought it looked good,
                it tasted bad: you realize that you've been had.</p>
            <p>Skiing in the Wasatch lately is best described in terms of the
              Tupperware Fake-Out.</p>
            <p>As Helm&uuml;t and I skied towards the mountain we saw a good
              looking plate of cookies, barely visible, up near the summit. Despite
              the fact that most of the good cookies around the Wasatch have
              been thoroughly devoured, this plate looked full and very promising.
              We had at least a couple thousand feet to climb up to it, but there
              would be plenty when we got there. Our appetites soared.</p>
            <p>There were lots of crumbs
              on the way up to the plate. We didn't' stop to taste them, but
              as even the crumbs looked good, we figured that things could
              only get better. As we got closer, we could see more of the beckoning
              cookies. We could practically taste the freshness. Maybe we should
              have been better at picking up the clues: the cookies were all
              too perfect looking to be homemade, too similar in size, maybe
              a little dry looking, as if they'd been on the shelf growing stale.
              But we chose to ignore these details. Instead we put our heads
              down and climbed, allowing our minds to imagine creamy smoothness.
              With the plate so near, our mouths began to salivate. </p>
            <p>As the entire, full plate of cookies came into view the worst
              possible thing happened: Wasatch Powder Birds came out of nowhere
              and landed on top of our plate of cookies. In horror, we watched
              as the WPB guide offered the plate to his clients. We moaned as
              we witnessed the plate slowly emptying before our eyes. Most of
              the clients were polite and only ate one cookie. But then we watched
              the last client selfishly attack the plate. It was apparent that
              he'd never seen a plate of cookies like this before. He grabbed
              as may as he could and stuffed them into his mouth.
              In a greedy frenzy, he jammed them into the many pockets of his
              parka and even down into his pants. Cookies were dropping from
              his clumsy, swollen hands and rolling down the mountain leaving
              little pock-mark trails of crumbs. It was horrible. We were convinced
              that there'd be nothing left.</p>
            <p>But there was a little luck on our side. The WPB group moved on
              in search of bigger plates of cookies to conquer, and left two
              untouched cookies behind. We raced up the last 500ft to the
              plate, our joy returning. We only really had time for one each
              anyway, so the remains  would suffice. </p>
            <p>As we got closer,
              we realized that they'd left the best two cookies.
              They were bigger cookies, fluffier looking. Finally we got to the
              top of the plate, saliva pooling out of control. We peeled our
              skins. We zipped our zips. We lifted off the tupperware lid. And
              in an excited rush, we each dove head-first onto the cookies,
              simultaneously taking big mouthfuls. </p>
            <p>Like a smack up-side the head, we tasted the worst
              cookies imaginable. Big, nasty, tooth rattling things with appalling
              layers of hidden crusts, haphazard textures, and big, un-savory
              nuggets. The awful taste was relentless, we couldn't get it out
              of our mouths. We kept eating hoping the taste would improve, but
              it didn't. And like children caught red-handed, we realized that
              we'd been had by a humongous, self-inflicted Tupperware Fake-Out. </p>
