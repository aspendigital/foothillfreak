            <h1>Terms of Endearment</h1>
            <dl>
              <dt>All-whiner<a name="Alwhiner" id="Alwhiner"></a></dt>
              <dd>Noun. Alpine Skier. </dd>
              <dt>Altoid</dt>
              <dd>Noun. Alta local.</dd>
              <dt>Beater<a name="beater" id="beater"></a></dt>
              <dd>Noun. Flailing newbie.</dd>
              <dt>Betty<a name="betty" id="betty"></a></dt>
              <dd>Noun. Female rider. Often one who puts more energy into snow
                fashion than snow riding.</dd>
              <dt>Crack of nooner<a name="Crack_of_nooner" id="Crack_of_nooner"></a></dt>
              <dd>Noun. Skier who (luckily for powder lovers) prioritizes
                sleep over first chair.</dd>
              <dt>Fruit booter<a name="fruit_booter" id="fruit_booter"></a></dt>
              <dd>Noun. A skateboarder will tell you that a fruit booter is
                a roller-blader. A skier will tell you that a fruit booter is
                someone on mini-skis. A snowboarder will tell you that it's anyone
                who wears hard boots.</dd>
              <dt>Gaper<a name="gaper" id="gaper"></a></dt>
              <dd>Noun. A person that enjoys standing on snow and watching
                from a safe (or not so safe) distance. </dd>
              <dt>Grommet, Grom<a name="grom" id="grom"></a></dt>
              <dd>noun. Little rider. Named after the little rubber bushing found
                on skateboard trucks.  </dd>
              <dt>Jibber<a name="jibber" id="jibber"></a></dt>
              <dd>Noun. One who focuses on jumping and tricks. Tend to block
                runs or congregate in wads.</dd>
              <dt>Jibblets<a name="jibblets" id="jibblets"></a></dt>
              <dd>Noun. Scaled down version of a jibber. </dd>
              <dt>Knuckle Dragger<a name="knuckle_dragger" id="knuckle_dragger"></a></dt>
              <dd>Noun. Snowboarder. In particular, one who rides with cro-magnon, mad steeze. </dd>
              <dt>Moderate wind<a name="moderate_wind" id="moderate_wind"></a></dt>
              <dd>Noun. Person on chairlift that won't shut up.</dd>
              <dt>Newbie, Noob<a name="newbie" id="newbie"></a></dt>
              <dd>Noun. If you don't know what this word is than you definitely are one.</dd>
              <dt>Poacher<a name="poacher" id="poacher"></a></dt>
              <dd>Noun. A snow rider who's choice of timing and terrain offends
                other riders, ski patrollers, gapers, land owners, wildlife,
                religious figures, friends, and/or local authorities.</dd>
              <dt>Shred Betty<a name="shred_betty" id="shred_betty"></a></dt>
              <dd>Noun. <a href="#betty">Betty</a> of
                remarkable riding skills.</dd>
              <dt>Sled Necks<a name="sled_necks" id="sled_necks"></a></dt>
              <dd>Noun. Snow-mobiler.</dd>
              <dt>Touron<a name="touron" id="touron"></a></dt>
              <dd>Noun. A tourist that is also a moron.</dd>
              <dt>Tray racer<a name="Tray_racer" id="Tray_racer"></a></dt>
              <dd>Noun. Snowboarder.</dd>
              <dt>Rat Stabber<a name="rat_stabber" id="rat_stabber"></a></dt>
              <dd>Noun. Flailing skier (notoriously of the telemark species)
                that points their ski poles in odd and dangerous directions while
                skiing.</dd>
              <dt>Wannabee<a name="wannabe" id="wannabe"></a></dt>
              <dd>Noun. I'm not sure what this means. I was hoping you could
                tell me. While you're at it, do you know any good places to ski?
                Can I go with you?</dd>
              <dt>Wad<a name="wad" id="wad"></a></dt>
              <dd>Noun. Congregation of snowboarders sitting on the slope,
                usually in an annoying position blocking from above or hidden
                down below a slope feature.</dd>
				<dt>Weekend Warrior</dt>
