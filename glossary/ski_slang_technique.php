            <h1>Ways to Ride/Ways to Tour</h1>
            <dl>
              <dt>Arlberg Technique</dt>
              <dd>noun. The first technique  of turning on skis without
                the use of a rudder, trees or falling. Developed in Arlberg,
                Austria.</dd>
              <dt>Bust</dt>
              <dd>Adjective. To do whole heartedly. </dd>
              <dt>Boost</dt>
              <dd>Verb. To go airborn with a mostly vertical path of trajectory. </dd>
              <dt>Bonk</dt>
              <dd>Verb. To strike a non H<sub>2</sub>O surface (such as a rock,
                tree or stump) with your board, boards, or inner tube, either
                strategically, or accidentally. </dd>
				<dt>Cracker Walk</dt>
				<dd>Verb. Trying to walk on you skis when your heels are locked down. Telemarkers turned Alpiners are painfully aware of doing the cracker walk</dd>
              <dt>Dawn Patrol</dt>
              <dd>Verb, noun. To extricate oneself  from bed at ungodly hours
                of the morning in an attempt to bring some life to a mind numbing
              corporate existence. See also Weekend Warrior. </dd>
              <dt>Dipsy Doodle</dt>
              <dd>Noun. The first skiing technique developed to ski short radius
                turns in deep powder. Origin: Alta, 1948ish. To travel a zig
                zagging course, like a screw ball in baseball. Note: those who
              ski deep hounds powder may be familiar with the term screw ball.</dd>
              <dt>Dusk Patrol</dt>
              <dd>Noun. Slacker version of the Dawn Patrol. Also
                good for full moon evenings and first dates. </dd>
              <dt>Farming</dt>
              <dd>Verb. Powder conservation technique. Turns are kept close
                together in order to allow for more, hyper controlled powder
                skiing. Approach used by guides to corral obedient powder thirsty
                clients. Farming may also be used as a landscape art form, in
                which the farming artist creates a wavy, van goghesque pattern
              on slopes for all to admire... Or laugh at. See Also <a href="#">Spooning</a>.</dd>
              <dt>Huck</dt>
              <dd>Verb. To jump off a preferably large precipice with more concern
                for the air time than on the inevitable landing and/or impact.</dd>
              <dt>Huck your meat</dt>
              <dd>Verb. A super sized huck with blatant loss
                of concern over bodily
                injury.</dd>
              <dt>In the back seat</dt>
              <dd>Adjective. To ski with your butt hanging over the tails of
                your skis.</dd>
              <dt>jib</dt>
              <dd>Verb. To ride with an emphasis on waiting in line to hit select
                terrain features. </dd>
				<dt>One n' Done</dt>
				<dd>Noun. A ski tour that involves one ascent and one descent. Converse of yo-yo-ing.</dd>
              <dt>Paramark</dt>
              <dd>Verb. to ski free heel equipment with no intention of ever
                dropping the knee. Also known as telellel.</dd>
              <dt>Pat the Dog</dt>
              <dd>Verb. To ride a snowboard in fear of removing your hands from
                the ground. See also <a href="#">Knuckle Dragger</a>. </dd>
              <dt>Poach</dt>
              <dd>Verb. To cut in front of another, riding their line before
                they can get to it.</dd>
              <dt>Post Hole</dt>
              <dd>verb. to sink up to your knee or deeper in snow. Usually happens
                when booting up a powdery snowpack, but under extreme snow conditions,
                can occur with skis on as well.</dd>
              <dt>Snake</dt>
              <dd>verb. Like poaching, but with more strategic buildup. For example,
                getting others to focus on the natural beauty of the mountains
                while you quietly take off your skins.</dd>
              <dt>Spooning</dt>
              <dd>Verb. Making turns in powder directly next to another set of
                turns.</dd>
              <dt>Stick</dt>
              <dd>Verb. To ski a line or land a jump without falling. See also
                stomp.</dd>
              <dt>stomp</dt>
              <dd>Verb. snowboarder version of stick.</dd>
              <dt>Straight Line (figure 11s)</dt>
              <dd>The ultimate method of skiing that not only reduces the distance
                between point A and point B, but conserves powder and deters
                the development of moguls. </dd>
              <dt>Telellel</dt>
              <dd>Verb. When telemark skiers pretend to be on Alpine equipment.
                See also Paramark.</dd>
              <dt>Eighting, Figure Eighting</dt>
			  <dd>Making turns in powder that overlap
                and mirror an existing set of tracks. A type of farming that
                optically resembles a stacked column of number 8s.</dd>
				<dt>Yo-Yo-ing</dt>
				<dd>To make multiple laps in the same slope. Phenomonon that often leads to farming.</dd>
            </dl>
