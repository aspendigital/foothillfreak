          <h1>The World's Most Essential  Glossary
            of Skiing terms + Ski Slang</h1>
          <h2>Terms of Endearment</h2>
          <p><span class="subhead">&nbsp;</p>
          <p><span class="subhead">Alwhiner.</span><a name="Alwhiner" id="Alwhiner"></a></span>. <span class="caption">noun.</span> Alpine Skier. </p>
          <p><span class="subhead">Beater<a name="beater" id="beater"></a>.</span></span>. <span class="caption">noun.</span> Flailing
            newbie. </p>
          <p><span class="subhead">Betty.</span><a name="betty" id="betty"></a></span>. <span class="caption">noun.</span> Female
            rider. Often one who puts more energy into snow fashion than snow
            riding.</p>
          <p><span class="subhead">Crack of nooner.</span></span>. <span class="caption">noun.</span> Skier who (luckily for powder lovers) prioritizes
            sleep over first chair. </p>
          <p><span class="subhead">Fruit booter</span>. <span class="caption">noun.</span> Anyone who wears hard boots.</p>
          <p><span class="subhead">Gaper</span>. <span class="caption">noun.</span> A person that enjoys standing on snow and watching
            from a safe (or not so safe) distance.</p>
          <p><span class="subhead">Jibber</span>. <span class="caption">noun.</span> One who focuses
            on jumping and tricks. Tend to block runs or congregate in wads.</p>
          <p><span class="subhead">Jibblets</span>. <span class="caption">noun.</span> Scaled down version of a jibber.</p>
          <p><span class="subhead">Knuckle Dragger</span>. <span class="caption">noun.</span> Snowboarder.
            In particular, one who rides with a cro-magnon flair. </p>
          <p><span class="subhead">Moderate wind</span>. <span class="caption">noun.</span> Person on chairlift that won't shut up.</p>
          <p><span class="subhead">Newbie</span>. <span class="caption">noun.</span> If
            you don't know what this word is than you definitely are one. </p>
          <p><span class="subhead">Poacher</span>. <span class="caption">noun.</span> A
            snow rider who's choice of timing and terrain offends 
            other riders, ski patrollers, gapers, land owners, wildlife, religious
            figures, friends, and/or local authorities. </p>
          <p><span class="subhead">Shred Betty</span><a name="ShredBetty" id="ShredBetty"></a></span>. <span class="caption">noun.</span> Visually stimulating <a href="#betty">betty</a>            of remarkable riding skills.</p>
          <p><span class="subhead">Slednecks</span>. <span class="caption">noun.</span> Snow-mobiler. </p>
          <p><span class="subhead">Touron</span>. <span class="caption">noun.</span> A tourist that is also a moron.</p>
          <p><span class="subhead">Tray racer</span>. <span class="caption">noun.</span> Snowboarder.</p>
          <p><span class="subhead">Rat Stabber</span>. <span class="caption">noun.</span> Flailing
            skier (notoriously of the telemark species) that points their ski
            poles in odd and dangerous directions while skiing.</p>
          <p><span class="subhead">Wannabee</span>. <span class="caption">noun.</span> I'm
            not sure what this means. I was hoping you could tell me. While you're
            at it, do you know any good places to ski? Can I go with you? </p>
          <p><span class="subhead">Wad</span>. <span class="caption">noun.</span> Congregation
            of snowboarders sitting on the slope, usually in an annoying position
            blocking from above or hidden down below a slope feature.</p>
          <p class="style2">&nbsp;</p>
