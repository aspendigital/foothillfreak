            <h1>Highly Accurate Glossary of Ski Terms </h1>
            <h2>Introduction</h2>
            <p>On top of the massive cornice, my friend pointed excitedly. &quot;look,
              there's a pinhead!&quot;</p>
            <p>Pinhead? I'd never heard of a pin head before. And judging by
              this character, I had definitely never seen one before either.
              He was tall and lean, with a neat ponytail down to the small of
              his back. His chin hid behind a neat, thick beard. Snow shedding
              red fabric was wrapped around his ankles up to his knees, and his
              thighs were coated in duct tape. His tattered goretex jacket looked
              around 10 years old, despite the fact that it was 1990 and goretex
              had just recently appeared on the slopes. </p>
            <p>And he smelled. From where I was standing he reeked of the wood
              burning stove, as well as something rather sweet. </p>
            <p>Suddenly, he pointed his long wobbly skis down the ridge and was
              off. Upon reaching top speed, he arched a smooth turn. He dropped
              his inside knee all the way to the ski, leaned his weight forward,
              and launched off the cornice, heartily soaring into the thin winter
              air. </p>
            <p>I had no idea a human could survive such air time, much less subject
              themselves to it on purpose. And his incredibly skinny skis flapping
              about on the ends of his toes didn't look very helpful. I was pretty
              sure he had no intention of actually landing the jump, but while
              in the air he maintained excellent form . At the end of his 40
              foot free fall, he completely cratered. </p>
            <p>He rebounded quickly. Beard white with snow, both arms shot up
              into the air and a triumphant &quot;woooo hooooooo&quot; bellowed
              forth. He promptly dug out a pole, righted his goggles, put a stray
              ski back on, and was off again. He descended the rest of the bowl
              while feverishly dropping a knee in each round turn. Then the first
              telemark skier I'd ever seen promptly dissappeared, leaving nothing
              but a trail of spindrift and a large legacy in his wake. </p>
            <p>&quot;So that's what a pinhead is.&quot; I thought.  &quot;I like
              pinheads!&quot;<br />
            </p>
            <p>15 years later, I'm still learning new words that help explain
              this incredible sport of sliding on snow. Growing waves of newcomers,
              snow sliding super stars and ancient powder stash die-hards continue
              to add to this colorful language. Escalating feats of human endeavor
              as well as technological advances also fuel the need for new terms.
              And sitting on a chairlift for 9 minutes while anxiously awaiting
              the next run continues to be the perfect incubator for creating
              new ways of talking about riding.</p>
            <p>In the past five years, new ways of communicating have drastically
              affected the growth of the sport. The internet has become an incredible
              asset to the skiing community, allowing up to the minute trip reports,
              mountain cams, and even snow data that's free of resort marketing
              influence. This new technology also allows creative jargon to go
              from local slope to national awareness within moments. Nothing
              is sacred in a snow riders chat group, but the way to gain respect
              is through impressive feats of riding, as well as through the usage
              of creative lexicon.</p>
            <p>The growth in the ski and snowboard industry has helped created
              a new glossary. At the same time, other words begin to disappear
              from lack of use. One evening, I stumble upon an internet chat
              thread discussing the term &quot;pinhead&quot;. The group's dialog
              starts off with a newbie asking what one is. Others join in, having
              no idea what the term means or where it comes from. Eventually
              someone explains how in days of old, tele bindings had pins that
              inserted into the toe of the boots, which were then clamped to
              the ski. Another in the forum reminisced about the skinny skis
              and the leather boots. Another the beard and the brown bag lunch.
              Slowly, from collective conscience, the fabled pinhead is recreated
              and the newbie gets the picture. </p>
            <p>A fearsome truth has rears it's head. As styles, technologies,
              and people change, so does the oral tradition of gliss. While brilliant
              new ways of sharing experience are created, old methods run the
              risk of being lost forever.<br />
            </p>
            <p>But never fear! With this ski glossary in hand, our wonderfully
              colorful language is safe at last. This small booklet is completely
              designed to function as a dialectical time capsule, providing you
              with years and years of enjoyment from reading about terms like &quot;snow
              squeegee&quot; and &quot;poodle factor&quot;. It's also completely
              educational with origins and additional fascinating facts documented
              whenever possible. </p>
            <p>Some of these words may be new to you. Others may be so ingrained
              that they seem like common knowledge. Read it, enjoy it, and be
              sure to add some fables of your own.</p>
