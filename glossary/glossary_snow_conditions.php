            <h1>Snow Conditions</h1>
            <dl>
			<dt>Boiler Plate</dt>
			<dd>Adjective. Mostly, but not completely supportable.</dd>
			
            <dt>Breakable Crust</dt>
			<dd>Noun. Snow with a crusty surface that has a less
              than 50% chance of supporting you in a turn.</dd>
			  
            <dt>Bullet Proof</dt>
			<dd>Adjective. Solid. Supportable. Also used to describe men's windbreaker briefs.</dd>
			
            <dt>Cauliflower</dt>
			<dd>Noun. Chunk of man made snow. A supersized, bullet
              proof Death Cookie.</dd>
			  
            <dt>Champaign Powder</dt>
			<dd>Noun. Low water content snow.</dd>
			
            <dt>Character Building</dt>
			<dd>Adjective. Desperate and miserable conditions.
              Generally used in failed attempts to boost morale.</dd>
			  
            <dt>Collapsing</dt>
			<dd>Adjective. Snow that shifts underfoot indicating a
              weak layer in the snow pack. Also known as whoomping.</dd>
			  
            <dt>Crud</dt>
			<dd>Noun. Tracked powder in various of states of decline.</dd>
			
            <dt>Death Cookie</dt>
			<dd>Noun. chunk of snow that you think you can plow
              through, but turns out to be solidly lodged in place. Any form
              of icy shrapnel that appears out of nowhere.</dd>
			  
            <dt>Dust on Crust</dt>
			<dd>Noun. A new soft snow layer over icy slopes that
              is thin or low density enough that rider still senses the crusty
              surface.</dd>
			  
            <dt>Face Shot</dt>
			<dd>Adjective. Phrase used by others to describe the snow
              conditions when you weren't there.</dd>
			  
            <dt>Firn</dt>
			<dd>Noun.</dd>
			
            <dt>Graupel</dt>
			<dd>Noun. Type of snow flake that falls in the shape of a
              solid ball. Often seen at the beginning or turbulent moments of
              a snow storm.</dd>
			  
            <dt>Groomer</dt>
			<dd>Noun. Snow that has been Freshly rolled over and groomed
              by a snow cat.</dd>
			  
            <dt>Hero Snow</dt>
			<dd>Noun. A snowpack that allows for suprisingly fluid
              and easy skiing due to it's &quot;right side up&quot; consistency.</dd>
            
			<dt>Loud Powder</dt>
			<dd>Noun. Ice.</dd>
			
            <dt>Magazine Crust</dt>
			<dd>Noun. Zipper crust that is so thick that strapping
              magazines to your shins is recommended to assist with pain management
              and breaking the ice.<br />
              [ORIGIN: Utah Avalanche Forecaster Bruce Tremper]</dd>
			  
            <dt>Mashed Potatoes</dt>
			<dd>Noun. Highly technical jargon meaning mushy.</dd>
			
            <dt>N&eacute;v&eacute;</dt>
			<dd>Noun.</dd>
			
            <dt>Packed Powder</dt>
			<dd>Noun. Term used solely by ski area marketers which
              indicates anything from definitely not powder to absolutely not
              powder.</dd>
			  
            <dt>Powder</dt>
			<dd>Noun. AKA pow. You wouldn't want to know.</dd>
			
            <dt>Rain</dt>
			<dd>Crust. Noun. Crusty layer of ice that appears on the surface
              of the snow after a rain followed by freezing temperatures. May
              be, or may not be supportable.</dd>
			  
            <dt>Right Side Up</dt>
			<dd>Noun. Snow that bonds well, creating safe conditions
              as well as excellent riding conditions. A study of the layers would
              indicate lighter density snow and cooler temperatures near the
              surface of the snow pack.</dd>
			  
            <dt>Rime</dt>
			<dd>Noun. Supercooled water drop that freezes to an object,
              creating an opaque white coating.</dd>
			  
            <dt>Sierra Cement</dt>
			<dd>Noun. Snow with high water density. Firm powder,
              not mushy like mashed potatoes. Coming from Utah, this is hard to believe.</dd>
			  
            <dt>Snorkel</dt>
			<dd>Adjective. Snow so light in density that while skiing
              it goes airborne and creates respiration difficulty. See also face
              shot.</dd>
			  
            <dt>snotsicle</dt>
			<dd>Noun. stalactite consisting of various porportions
              of frozen water droplets and mucosa.</dd>
			  
            <dt>Snow Snake</dt>
			<dd>Noun. Suborder: Ophidia (or Serpentes). Order: Squamata.
              Family: piste montis. White, highly camoflaged limbless reptile
              found on piste in ski areas, know to prey on gapers, beaters, and
              hot doggers alike.</dd>
			  
            <dt>Sun Cups</dt>
			<dd>Noun. Late spring into summertime snow phenomenon where
              melting caused by the sun creates a consistently pock marked and
              icy surface. Large sun cups are conducive to character building.</dd>
			  
            <dt>Sun Crust</dt>
			<dd>Noun. Icy snow surface layer of various thicknesses
              created by exposure to the sun.</dd>
			  
            <dt>Supportable</dt>
			<dd>Noun. When the top layer of an inverted or and/or
              crusty snowpack is strong enough to keep a rider from punching
            through to deeper, weaker layers.</dd>
			
            <dt> Surface Hoar</dt>
			<dd>Noun. recrystalized powder surface that resembles
              feathers from 1 to six inches in vertical length. Created when
              moisture freezes as it moves up and out of the snowpack. Often
              found by streams or in cold pockets, but after several cold, clear
              nights, can be found covering entire slopes.</dd>
			  
            <dt> Sastrugi</dt>
			<dd>Noun. Sounds Italian but it's Russian. Sculpted ridges
              that form on wind eroded snow.</dd>
			  
            <dt> Unsupportable</dt>
			<dd>Noun. When the top layer of an inverted or and/or
              crusty snowpack is not strong enough to keep a rider from punching
              through to deeper, weaker layers. Phenomenon conducive to creative
              word usage.</dd>
			  
            <dt>Upside Down</dt>
			<dd>Noun. A snowpack that is stronger, more
              dense near the surface, and both weaker and lighter density as
              you go deeper. Often created from a single storm that starts cold
              and finishes warm.</dd>
			  
            <dt>Whoomping</dt>
			<dd>Adjective. A weak, unsupportable snowpack
              that collapses not just underfoot, but extending out. Creates a
              whoomphing sound as the layers settle. verb. When the snow audiably
              settles. </dd>
			  
            <dt>Wind Buff</dt>
			<dd>Noun. A wind affected snow surface that allows for
              suprisingly good riding. </dd>
			  
            <dt> Wind Slab</dt>
			<dd>A wind affected snow surface that allows for suprisingly
              bad, and potentially dangerous riding.</dd>
			  
            <dt>Zipper Crust</dt>
			<dd>When a sun,
              rain, humidity or temperature event creates a thin surface crust
              over powder. Thicknesses run from paper thin to pencil thin. </dd>
