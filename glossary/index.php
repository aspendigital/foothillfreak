            <h2>Terms from the Origional Freaktionary</h2>
            <dl>
              <dt>Avy Bait</dt>
              <dd><em>Noun.</em> Person
                with new BC equipment and no skillz. Seen in abundance
                on the first dump day after Christmas. Phenonomen particulary
                common among fringe winter sports enthusiasts including, but
                not limited to, extreme snow tubers (a special class of avy bait
                in themselves). Best avoided. </dd>
              <dt>Corn Snow</dt>
              <dd><em>Noun</em>.
                Springtime snow condition in which the snowpack surface identically
                resembles the yellow rows and kernels of corn on the cob. Extra
                good corn conditions are likely to resemble the &quot;salt and
                pepper&quot; corn
                variety, though some prefer &quot;indian corn cob&quot; snow
                conditions. Hominey is another excellent corn sub-class, but
                Polenta is best avoided. Don't try eating corn snow though as
                it tastes like brussel sprouts.</dd>
              <dt>De-veining</dt>
              <dd><em>Verb</em>. To loose control while riding a narrow patch
                of snow and spin off into the rocks, grass, or native fauna.
                See veining.</dd>
              <dt>Freakin:</dt>
              <dd><em>Verb.</em> To hike, climb, ski, rappel or generally flail
                about with skis or snowboards on low elevation hills with the
                main intent of finding snow and/or angled surfaces upon which
                to slide back down upon.</dd>
              <dt>Fruit Booters</dt>
              <dd>Noun. (Skateboardese dialect, Durango region, 2000 A.D.) Rollerbladers.
                On snow, this term is applicable to mini-ski skiers.</dd>
              <dt>Half-a-lapper</dt>
              <dd><em>Noun.</em> One who starts their descent from a lower elevation
                (hence a lesser position) than the proper top of the skin track
                or summit. A despicable creature known to &quot;poach&quot; your line while
                you're up ahead breaking trail. If you are tempted to adopt &quot;half-a-lapper&quot;
                behaviors, remember the wise words of world famous ski guide
                Szen Shushler, &quot;Vee Ske from Ze Summit, and Ze Summmit is Ze
                Top!&quot;. </dd>
              <dt>Hoar Frost</dt>
              <dd><em>Noun.</em> The perfect eyeshadow for an 80's themed get
                together.</dd>
              <dt>Gaper</dt>
              <dd><em>Noun.</em> First documented use in Aspen, CO, 1986. A person
                on the slope who spends a lot of time looking, often stopping
                in annoying, inappropriate, or downright dangerous places to
                do so. Refers to the dropped jaw status of the onlooker. Worthy
                of praise as they keep ski resorts in business by actually buying
                lift tickets and lunch in the cafeteria.</dd>
              <dt>Gaper Block</dt>
              <dd><em>Noun.</em> A congregation
                of gapers clogging
                access to inappropriate or downright dangerous lines.</dd>
              <dt>Gaper Gap</dt>
              <dd><em>Noun.</em> The exposed space on the forehead found between
                the top of one's ski goggles and the front of one's hat or helmet.
                Note: distance and total area of said gaper gap are generally
                proportional to an individual's relative gaper status.</dd>
              <dt>Groomer</dt>
              <dd><em>Noun.</em> A destructive variety of <a href="#gaper">gaper</a>.
                A skier or rider in a tight chute who &quot;grooms&quot; fresh powder snow
                by sideslipping the complete width of an entire chute. Also known
                as the &ldquo;snow squeegee&rdquo; or making boarduroy. (gaper and groomer
                derogatory terminology taken and edited without permission from <a href="http://departments.cvuhs.org/justinc/teleclub/ski_slangs.htm">http://departments.cvuhs.org/justinc/teleclub/ski_slangs.htm</a>,
                thanks!)</dd>
              <dt>Knuckle Dragger</dt>
              <dd><em>Noun.</em> Snowboarder. Used frequently by skiers, especially
                when poached by a boarder. <em>AKA:</em> Tray racer, ski-boarder,
                dog patter, and sorry-dude.</dd>
              <dt>Moose Horns</dt>
              <dd><em>Adjective.</em> When a telemark skier drops so low that
                they hold their pole handles up by their head. The effect is
                heightened when the poles are thrust out horizontally to the
                sides, thus creating the siloette of a fully racked moose plowing
                through the snow. Often used in combination with double pole
                plants and a large <a href="#Poodlefactor">poodle factor</a>.</dd>
              <dt>No Fall Zone</dt>
              <dd><em>Noun.</em> Slope angle description that implies huge ragdoll
                potential followed by certain and painful death. Frequently comes
                into use when members of the opposite sex are present, especially
                near &quot;Bettys&quot;. </dd>
              <dt><a name="Poodlefactor"></a>Poodle factor</dt>
              <dd><em>Noun.</em> The amount of fore and aft distance between
                the feet of a telemarker during a dropped knee turn. A large
                poodle factor is often found among beginner (or gaper) telemarkers.
                See Also Moose Antlers. I'm not making this shit up!</dd>
              <dt>Punjabi sticks</dt>
              <dd><em>Noun</em>. (From the Philipines). A pointed stick projecting
                out of the snowpack and up the hill. Found in natural environments
                and on angry property owners turf. Best avoided.</dd>
              <dt>Rat Stabber</dt>
              <dd><em>Noun.</em> Flailing telemark skier. Often seen pointing
                their poles in threatening positions. See also &quot;Moose Horns&quot;.                </dd>
              <dt>Rockin</dt>
              <dd><em>Verb.</em> To ride your board or boards across snowless
                areas regardless of potential damage to equipment, environment,
                participant, or innocent bystanders. </dd>
              <dt>Scotch Mists</dt>
              <dd><em>Noun.</em>            (thieving
                slang, first published in 1736) Raining like hell. (thanks to <a href="http://kokotele.com/hillrod.htm">http://kokotele.com/hillrod.htm</a>) </dd>
              <dt>Scraper</dt>
              <dd><em>Noun.</em> (from
                contemporary urban slang meaning pathetic automobile). Any ski
                or board that you should throw away or turn into furniture, but
                keep riding instead. Like a rock ski, but far worse. Usually
                features multiple binding mounting holes, blown edges, missing
                patches of p-tex, rotting wood and exposed fiberglass. Excellent
                for thin snowpacks and for making a statement on the tram. </dd>
              <dt>Ski Boardz</span></dt>
              <dd><em>Noun</em>. A snowboard
                that has been sawed through longitudinally and mounted with any
                type of ski bindings, thus creating a pair of low cost, low weight
                fat skis. The &quot;z&quot; on the end of &quot;board&quot; is
                an onomatopoeia indicating the fact that the board has been sawed,
                or &quot;zzzzzzz&quot;ed.</dd>
              <dt>Skookum-Chuck</span></dt>
              <dd>(From
                Pacific North West Native American) <em>Verb.</em> To ram a large,
                outstanding pillow of snow with the hopes of making it over and
                subsequently going airborne. <em>Noun.</em> A
                large and irregular mound of snow that forms over humped objects
                either natural or man-made. A kicker with extremely abrupt transitions.
                Also a small town in the Kootenay Rockies. </dd>
              <dt>Thumb Tack</span></dt>
              <dd> <em>Verb.</em> When
                a snowboarder lands head first into a deep snowpack and becomes
                lodged in place; unable to self extricate. Best performed with
                friends watching nearby.</dd>
              <dt>Under Control</span></dt>
              <dd>A<em>dv</em>. A term
                thats meaning is continually debated and completely relative.
                One man's &quot;under control&quot; is another man's 50mph complete
                yardsale accompanied by expletives and/or blood curdling screams.</dd>
              <dt>Veining</span></dt>
              <dd><em>Verb</em>.
                To ski a narrow line of snow that is closely boardered by dry
                ground. In the Wasatch foothills, long paths of snow are created
                by drifted snow that stay around long after the rest of the thin
                snow pack has melted.</dd>
              <dt>Weiner Wacker</dt>
              <dd><em>Noun</em>.
                <em>(Wasatch Oldschool).</em> Male version of Bush Wacker. A
                shrub that has been partially buried in snow so that it's wispy
                end is crotch level or higher. Found throughout the Wasatch in
                the early season and at lower elevations all season long. Depending
                on your disposition, either avoid them completely, or ski over
                them with one ski on each side. See also <em>Punjab stick/Bush
                Wacker.</em></dd>
            </dl>
