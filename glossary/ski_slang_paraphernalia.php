            <h1>Snow Sliding Paraphernalia</h1>
            <dl>
              <dt>Avalanche Cords</dt>
			  <dd>Noun. Before Avalanche beacons, skiers in avalanche
                terrain dragged avalanche cords behind themselves with the hopes
                that, in an avalanche, they would float to the surface to indicate
                their location if accidentally buried.</dd>
              <dt>Bear Trap</dt>
			  <dd>Noun. The first metal ski binding that replaced leather
                straps. Allowed for better performance as well as more leg injuries.</dd>
              <dt>Bondage Boards</dt>
			  <dd>Noun. Telemarkese for Alpine skiing equipment.</dd>
              <dt>Fag Bag</dt>
			  <dd>Noun. One piece suit.</dd>
              <dt>Fatties</dt>
			  <dd>Noun. Extra wide skis. Width is relative.</dd>
              <dt>Gaper Gap</dt>
			  <dd>Noun. The space between the top of ones goggles and
                the bottom of ones hat. Also, the amount of forehead that shows
                between goggles and hat. Generally speaking, the larger the gap,
                the greater the gaper.</dd>
              <dt>Klister</dt>
			  <dd>Noun. Sticky wax paste used on the bottom of skis for
                cross country skiing on wet or old snow.</dd>
              <dt>Logs</dt>
			  <dd>Noun. Acroynm for Long Old Gaper Skis. </dd>
              <dt>Plank</dt>
			  <dd>Noun. A ski or a snowboard.</dd>
              <dt>Poma Lift</dt>
			  <dd>Noun. A surface style lift designed by frenchman
                Jean Pomagalsk in 1935. Also called a &quot;rump-lifter&quot;,
                riding a poma lift involves quickly putting a round disk between
                the legs and pulled up to the buttocks before the spring loaded
                connection activates and launches you up the hill.</dd>
              <dt>Powder Cords</dt>
			  <dd>Noun. Bright orange or yellow cord tied to equipment
                in case of loss in a powdery yard sale. Good indicator of low
                DIN settings and/or beater status.</dd>
              <dt>Ski Board</dt>
			  <dd>Noun. What gapers called snowboards during the 80's
                and early 90's, before they became common knowledge.</dd>
              <dt>Skin Wax</dt>
			  <dd>Noun. Hard, slippery wax that is rubbed on skins to
                reduce friction and glopping on snow.</dd>
              <dt>Sled</dt>
			  <dd>Noun. Snowmobile.</dd>
              <dt>Sticks</dt>
			  <dd>Noun. Snowboardese for ski poles.</dd>
              <dt>Training Heels</dt>
			  <dd>Noun. Telemark word for alpine skiing equipment.</dd>
              <dt>Ullr</dt>
			  <dd>Noun. Norse god of winter. Often depicted as walking on
                skis. Unfortunately he was married to a snowshoeing goddess.</dd>
