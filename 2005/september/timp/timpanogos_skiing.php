            <h1>September Skiing on Mt. Timpanogos </h1>
            <h6>September 25th, 2005</h6>
            <p class="caption">12 months of skiing: Finale on Mt. Timpanogos. </p>
        <p>The idea has been in the back of my head
        for years. A handful of summers I even gave a it a half assed
        attempt, yet failed miserably. But this year, I finally pulled it together
        and skied 12 months in a row.</p>
		
