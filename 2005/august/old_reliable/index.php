            <h1><span class="Header">Old Reliable Snowfield
                  at Snowbird, on the Cheap </span></h1>
            <p><span class="Header">August 20th, 2005</span><span class="location"> </span></p>
            <p>&nbsp;</p>
            <p>A week ago I traversed
                a section of the little cottonwood/American fork ridge. From
                the top of American Fork Twin Peaks, I saw a posse of skiers
                taking one narrow line down Old Reliable in Little Cloud bowl.
Since everyone was skiing the same line, the snow actually appeared fresh, white,
                and mostly free of debris. It looked way more inviting than the
              manky, brown, rock strewn snow that lightly covered other portions
              of the wasatch.</p>
            <p>That's when it hit me. We could easily hike up
              to the snow patch, ski till our hearts content, and then bust a
              free ride down on the tram, easily saving up 2,500 feet of downhill
              ski carrying. </p>
            <p>Today we tried out the cheap and easy plan,
              and it worked great.<span class="location"> <br />
            </span></p>
            <img src="images/DSC00922.jpg" width="600" height="408" /><br />
              <span>It was too hot to sit in a car, so foothill
              took the pedal powered air conditioned mobile to the meeting place.
              With the skies loaded, the bike didn't turn very well, but it went
              straight
              just fine.</span></p>
            <p><span><br />
            </span></p>
            <img src="images/DSC00928.jpg" width="600" height="478" /><br />
              <span>On our way up people, out of disbelief,
              kept asking us where we were going to go skiing. So it was a bit
              of
            a relief when we finally saw snow in the distance.</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00933.jpg" width="600" height="459" /><br />
              <span>We all had our own, personal burden to carry...</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00929.jpg" width="600" height="472" /><br />
              <span>On the ascent, one of the summit team members
              bonked hard and required an extra long re-fueling session, not
              to mention a diaper change..</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00939.jpg" width="450" height="600" /><br />
              <span>When traveling to life's snow patches,
              be sure to stop and smell the lupine.</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00950.jpg" width="450" height="600" /><br />
              <span>No   toddlers were harmed in
              the taking of this photo...</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00955.jpg" width="600" height="450" /><br />
              <span>PowderPrincess brutalizes her floppy girl
              skis on the pimply snow face. </span></p>
            <p><span>While skiing the first run, its possible
                that non-pure thoughts might have slipped into my mind. These
                thoughts might have sounded like this, &quot;This snow sucks. This isn't
                worth it. I could be cleaning my room. I think I'm getting sun
                burnt. it's too bumpy. This snow sucks. Etc. Etc..&quot; But
                my second and third runs were mostly comprised of that nirvanic
                state
                that makes skiing the best sport in the world, even in august.
                </span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00956.jpg" width="600" height="439" /><br />
              <span>Speaking of snow acne, this line had
              it all: black heads, white heads, pink heads, freckles, scabs,
              and even a few skier swallowing craters. Being the excellent snow-dermatologist
              that I am, I highly recommend a completely fresh, 4 foot dump,
              and call me in the morning (around 6am).</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00964.jpg" width="600" height="450" /><br />
              <span>The short traverse to the tram.</span></p>
            <p>&nbsp;</p>
            <img src="images/DSC00970.jpg" width="600" height="450" /><br />
              <span>WonderWassa and T-rex on our way down the
              bird. It was T-rex's first ride on the tram. As a
              friend on board pointed out, her next time will be riding up!</span></p>
            <p>The trip was a success. We firmly decided that
              a 2,500 vertical foot, dry-land approach isn't bad at all when
              you don't have to carry your skies back down. Additionally, exposing
              T-rex
              to
              August
              skiing
              at such a young age is critical to her future as a foothill freaklet.
              Maybe, when she grows up, she'll see even more  year round potential
              in skiing than we ever dreamed of...</p>
            <p>&nbsp;</p>
