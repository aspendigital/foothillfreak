            <h1>About the foothill freak ski blog</h1>
            <p>
              For purely scientific purposes, (and because he has a shorter
                memory than a goldfish) the freak has decided to maintain a ski
                blog. To see what's happening and to see who's laid tracks where,
                follow these links to months of freakin action.</p>
            <h2>2007/2008 Ski Season</h2>
            <p>After getting off to a late start, this season has finally begun.
              October had some early dumps that made for decent snowboard descents
              and lots of early optimism. Then a huge high-pressure ruined November,
              rotting out the snowpack, forcing ski resorts to open two weeks
              later planned, and keeping the snow freaks confined to the few
              areas where snow was to be found: in front of snow making equipment.</p>
            <h2>2006/2007 Ski Season<br />
            </h2>
    <p>It was another poor year for foothill skiing. The Upper reaches
              of the typically powdery Wasatch were also less than optimal, thus
              inspiring the <a href="2007/january/kessler_peak/catchers_mitt.php">Tupperware
              Fake Out Report</a>. But true freaks know
              how to turn limons into limonade. A massive low-pressure was a
              good excuse to try a new trans-canyon route starting at <a href="2007/january/little_pine/index.php">Little
              Pine Chute</a>. Then, when the snow finaly returned, we happed
              to be up at <a href="2007/february/baldy_knoll/index.php">Baldy
              Knoll Yurt</a>, where suprisingly stable conditions lead
              us to the steeps. In March, our group of freaks was the first to
              arrive at the stunning remains of a massive <a href="2007/march/Gobblers_knob_avalanche/index.php">Gobblers
              Knob Avalanche</a>. In May, we finally summitted the elusive <a href="2007/may/mount-nebo/index.php">Mount
              Nebo</a>, and then went to <a href="2007/may/tushar-mountains/gold-mountain.php">The
              Tushar Mountains</a> in the Feelmore Virgin Beaver area, for an
              inspirational recon.</p>
            <p>The stunningly sparse snowpack, and early, warm temps pretty much
              killed the spring ski season so many plans had to be put on hold.
              Instead, energies were directed to a <a href="2007/september/andes/index.php">South
              American Skiing Road Trip</a>, that we completed in September.</p>
            <h2>2005/2006 Ski Season</h2>
    <p>Another poor year for foothill skiing, but quiet an epic year
              in the upper Wasatch. The season didn't get started in force until
              shortly after Thanksgiving, but it was well worth the wait. Small
              but frequent storms freshened up the mountains seemingly every
              other day. Constant storms and fair temperatures kept the snowpack
              very stable. Across the range folks went off and skied almost
              everything imaginable. Spring skiing was excellent, though a very
              hot summer made year round skiing increasingly difficult come August
              and September. </p>
            <p>Hightlights of this season include a trip to the <a href="2006/april/fairymeadow/fairy_meadows.php">Fairy
                Meadows Hut</a> in British Columbia Canada, a stay at the <a href="2006/march/tetons/commissary.php">Commisary
                Ridge Yurt</a>              in the Tetons, a weekend in the <a href="2006/may/deep_creeks/deep_creeks.php">Deep
                Creek Mountains</a>, a visit
              to <a href="2006/may/silverton/Ophir_pass_skiing.php">Silverton
              Colorado</a>, and a quick trip back to <a href="2006/july/mount_adams/mount_adams.php">Mt.
              Adams Washington</a>.
              Excellent day tours of this season took us to <a href="2006/february/boxelder/box_elder_peak.php">Box
              Elder Peak</a>, Neff's
              Canyon Chutes, Snowbird's <a href="2006/june/pipeline/snowbird_pipeline.php">Pipeline
              Couloir</a>, and to Brightons <a href="2006/may/seagull/mount_tuscarora.php">Seagull
              Couloir</a>. </p>
            <h2>2004/2005 Ski Season</h2>
            <p>
              Bad year for snow down on the
              low down. Voodoo for snow below 7000 ft didn't work. But the upper
              peaks received
              epic porportions of powder all season long, providing one of the
            most consistently skiable seasons in remembered history.</p>
            <p>Winter arrived in earnest with an 80 inch base
              at Collins by the end of October. Team freak made it's first trip
              to Canada to the <a href="2005/march/kokanee/index.php">Kokanee
              Glacier Cabin</a>, an event that is likely
              to become an annual event. One of the team freak members was even
              featured on a national news report on the hazards of avalanches.</p>
            <p>This season was not only blessed with deep snowfall in the upper
              Wasatch, but it also maintained a relatively cool summer. It was
              an excellent year to ski every month of the year. The Freak celebrated
              his 12 month of skiing in a row on a colorful September day on
              <a href="2005/september/timp/timpanogos_skiing.php">Mount Timpanogos</a>. </p>
            <h2>2003/2004
              Ski Season</h2>
            <p>
              It was a good winter for the
              foothills. A huge, low elevation dump over the christmas holiday
              wiped out powerlines
              and choked snow blowers throughout Salt Lake City. Shoveling and
              sliding on snow became a way of life for the entire city for 48
              hrs straight. Then the winter stayed cold, and the sun rarely came
              out from behind a protective film of clouds. Low elevation lines
              stayed snowy for most of the season, enabling freaks to link turns
              on rare wasatch gems like Antelope Island, Grandure Peak into Parleys,
            and E Street in the Aves.</p>
            <p>Powder and snowpack depths were preserved well into
              spring. Ridiculously safe avalanche conditions allowed many to
              push the envelope onto steeper terrain. Moguls formed in the Y
              Couloir, a whole new school of twin tippers entered the backcountry,
              yet there were no skier avalanche fatalities. Unless, of course,
              you count the snowboarders.</p>
            <p>However, spring in the Wasatch came way to hot and
              fast. Late March, April and May were ususually warm, preventing
              proper corn from corning and skiers from skiing. A freak storm
              in the end of May brought incredible powder, and a small crowd
              of diehards converged on the upper bowls of Alta. Then in June,
              July, and August, the snowpack just completely went to shit. </p>
          <p>The rest, my fiends, is history.</p>
